    public void buscarUsuario(String id) {
        
        this.connection = new ConnectionFactory().getConnection();
        String sql = "";
        
        sql = "select * from usuario where id= '" + id + "'";
        
        try {
            PreparedStatement stm = connection.prepareStatement(sql);
            ResultSet res = stm.executeQuery();
            
           
            while (res.next()) {
                
                String tx1 = (res.getString(1));
                
                String tx2 = (res.getString(2));
                
                String tx3 = (res.getString(3));
                
                String tx4 = (res.getString(4));
               
                String tx5 = (res.getString(5));
               
                
                jTextField1.setText(tx2);
                jTextField2.setText(tx3);
                jTextField3.setText(tx4);
                jPasswordField1.setText(tx5);
               
                
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    public String atualizarUsuario(String id, String naturalidade, String nome, String email, String senha) {
        this.connection = new ConnectionFactory().getConnection();
        //Connection con;
        PreparedStatement ps;
        String status = "";
        try {
            ps = connection.prepareStatement("update usuario set naturalidade=?, nome=?, email=?, senha=?  where id =?" ) ;
            
            ps.setString(1, naturalidade);
            ps.setString(2, nome);
            ps.setString(3, email);
            ps.setString(4, senha);
            ps.setString(5, id);
                    
            int i = ps.executeUpdate();
            if (i != 0) {
                status = "Detalhes do cliente atualizado com sucesso";
                JOptionPane.showMessageDialog(null,status,"Atualização de Usuário",JOptionPane.INFORMATION_MESSAGE);
                buscarUsuario(id); 
                
           } else {
                status = "Detalhes do cliente não atualizados";
                JOptionPane.showMessageDialog(null,status,"Atualização de Usuário",JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return status;
    }
    
    public String deletarUsuario(String id) {
        this.connection = new ConnectionFactory().getConnection();
        PreparedStatement ps;
        String status = "";
        try {
            ps = connection.prepareStatement("delete from usuario where id=?");
            ps.setString(1, id);
            int i = ps.executeUpdate();
            if (i != 0) {
                status = "Usuario deletado do database";
                JOptionPane.showMessageDialog(null,status,"Deleção de Usuário",JOptionPane.INFORMATION_MESSAGE);
            } else {
                status = "Usuario não deletado do database";
                JOptionPane.showMessageDialog(null,status,"Deleção de Usuário",JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return status;
    }
    
    
